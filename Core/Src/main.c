/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  SSD_Init(&SSDcurrent, &hspi2, GPIOC, GPIO_PIN_6, &currentReading, 3);

  ADS1256_init(&ADS, &hspi3, ADS1256_CS_GPIO_Port, ADS1256_CS_Pin, ADS1256_RST_GPIO_Port, ADS1256_RST_Pin, adsRawData, adsData, &ADS1256_software_resetFlag);

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI2_Init();
  MX_ADC1_Init();
  MX_TIM17_Init();
  MX_TIM16_Init();
  MX_SPI3_Init();
  /* USER CODE BEGIN 2 */

  HAL_TIM_Base_Start_IT(&htim17); //Display refresh
  HAL_TIM_Base_Start_IT(&htim16); //ADC update

  /* USER CODE END 2 */

  /* Initialize leds */
  BSP_LED_Init(LED_GREEN);

  /* Initialize USER push-button, will be used to trigger an interrupt each time it's pressed.*/
  BSP_PB_Init(BUTTON_USER, BUTTON_MODE_EXTI);

  /* Initialize COM1 port (115200, 8 bits (7-bit data + 1 stop bit), no parity */
  BspCOMInit.BaudRate   = 115200;
  BspCOMInit.WordLength = COM_WORDLENGTH_8B;
  BspCOMInit.StopBits   = COM_STOPBITS_1;
  BspCOMInit.Parity     = COM_PARITY_NONE;
  BspCOMInit.HwFlowCtl  = COM_HWCONTROL_NONE;
  if (BSP_COM_Init(COM1, &BspCOMInit) != BSP_ERROR_NONE)
  {
    Error_Handler();
  }

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  HAL_ADC_Start(&hadc1);
  currentReading = (uint16_t)1234;

  ADS1256_SoftwareReset(&ADS);


  while (1)
  {

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

	  if(screenUpdateFlag == 1){
		  floatUpdateScreen(&SSDcurrent, floatCurrentReading, &numberWrittenFlag);
		  screenUpdateFlag = 0;
	  }

	  if(numberUpdateFlag == 1){
		  if(numberWrittenFlag == 1){

			 floatCurrentReading = HAL_ADC_GetValue(&hadc1);

			 floatCurrentReading = ((floatCurrentReading * 3.25)/4095);

			 numberWrittenFlag = 0;

		  }

		  numberUpdateFlag = 0;
	  }
	  if(ADS1256_DRDY_Flag == 1){

		  if(ADS1256_software_resetFlag == 1){
			  ADS1256_SoftwareReset(&ADS);
			  //no need to set flag to 0, the function handles this

		  }

		  ADS1256_Read(&ADS, &ADSdata);
		  ADS1256_Convert(&ADS, ADSdata, &adsReadingVolts);

		  ADS1256_DRDY_Flag = 0;
	  }

	  //ADS1256_SoftwareReset(&ADS);

	  //ADS1256_RegDump(&ADS, ADS1256regData);



  }
  /* USER CODE END 3 */
}

